const db = require('../config/db');
const notificationService = require('../services/notification.service');

// POST /api/admin/doctor-alerts
exports.createDoctorAlert = async (req, res) => {
  try {
    const { doctorId, averageRating = null, totalRatings = null, autoGenerated = false, message, type, cooldownDays: cd } = req.body || {};
    if (!doctorId) return res.status(400).json({ error: 'Thiếu doctorId' });
    const cooldownDays = parseInt(cd || 14);
    const THRESHOLD = 2.5;

    // Guard: check recent alert and new low ratings
    const [[recent]] = await db.query(
      `SELECT id, created_at FROM doctor_notifications 
       WHERE doctor_id = ? 
       ORDER BY created_at DESC
       LIMIT 1`,
      [doctorId]
    );

    if (recent && recent.id) {
      // If within cooldown -> block
      const [[withinCooldown]] = await db.query(
        `SELECT 1 AS flag FROM doctor_notifications
         WHERE doctor_id = ? AND created_at >= DATE_SUB(NOW(), INTERVAL ? DAY)
         ORDER BY created_at DESC LIMIT 1`,
        [doctorId, cooldownDays]
      );
      if (withinCooldown && withinCooldown.flag) {
        return res.status(409).json({ error: `Đã cảnh báo trong ${cooldownDays} ngày qua.` });
      }

      // Only allow if there are new low ratings since last alert
      const [[lowSince]] = await db.query(
        `SELECT COUNT(*) AS low_count
         FROM doctor_ratings
         WHERE dr_id = ? AND created_at > ? AND rating <= ?`,
        [doctorId, recent.created_at, THRESHOLD]
      );
      if (!lowSince || parseInt(lowSince.low_count || 0) === 0) {
        return res.status(409).json({ error: 'Chưa có đánh giá thấp mới kể từ lần cảnh báo trước.' });
      }
    }

    const notif = await notificationService.createDoctorNotification({
      doctorId,
      averageRating,
      message,
      type: type || 'low_rating',
      adminId: req.user?.id_u || null,
    });

    res.status(201).json({ success: true, notification: notif, autoGenerated: !!autoGenerated });
  } catch (e) {
    console.error('❌ createDoctorAlert error:', e);
    res.status(500).json({ error: 'Không thể tạo cảnh báo' });
  }
};

// POST /api/admin/doctor-alerts/auto-generate
exports.autoGenerateDoctorAlerts = async (req, res) => {
  try {
    const THRESHOLD = 2.5; // as confirmed by user
    const cooldownDays = parseInt(req.body?.cooldownDays || req.query?.cooldownDays || 14); // default 14 days

    // Get average rating per doctor
    const [rows] = await db.query(
      `SELECT dr_id, AVG(rating) AS avg_rating, COUNT(*) AS total
       FROM doctor_ratings
       GROUP BY dr_id`
    );

    let generated = 0;
    let eligible = 0;
    let skippedByCooldown = 0;

    let skippedNoNewLow = 0;
    for (const row of rows) {
      const avg = parseFloat(row.avg_rating || 0);
      const total = parseInt(row.total || 0);
      if (isNaN(avg) || total === 0) continue;

      if (avg <= THRESHOLD) {
        // Check cooldown: skip if there is a recent notification within cooldownDays
        const [[recent]] = await db.query(
          `SELECT id, created_at FROM doctor_notifications 
           WHERE doctor_id = ? 
           ORDER BY created_at DESC
           LIMIT 1`,
          [row.dr_id]
        );

        if (recent && recent.id) {
          // If within cooldown, skip
          const [[withinCooldown]] = await db.query(
            `SELECT 1 AS flag FROM doctor_notifications
             WHERE doctor_id = ? AND created_at >= DATE_SUB(NOW(), INTERVAL ? DAY)
             ORDER BY created_at DESC LIMIT 1`,
            [row.dr_id, cooldownDays]
          );
          if (withinCooldown && withinCooldown.flag) {
            skippedByCooldown += 1;
            continue;
          }

          // Only alert again if there are new low ratings since last alert
          const [[lowSince]] = await db.query(
            `SELECT COUNT(*) AS low_count
             FROM doctor_ratings
             WHERE dr_id = ? AND created_at > ? AND rating <= ?`,
            [row.dr_id, recent.created_at, THRESHOLD]
          );
          if (!lowSince || parseInt(lowSince.low_count || 0) === 0) {
            skippedNoNewLow += 1;
            continue;
          }
        }

        eligible += 1;
        try {
          await notificationService.createDoctorNotification({
            doctorId: row.dr_id,
            averageRating: avg,
            message: null,
            type: 'low_rating',
            adminId: req.user?.id_u || null,
          });
          generated += 1;
        } catch (e) {
          console.warn('⚠️ Could not create notification for doctor', row.dr_id, e.message);
        }
      }
    }

    res.json({ success: true, generated, eligible, skippedByCooldown, cooldownDays });
  } catch (e) {
    console.error('❌ autoGenerateDoctorAlerts error:', e);
    res.status(500).json({ error: 'Không thể tạo cảnh báo tự động' });
  }
};

// GET /api/admin/doctor-alerts?status=unread|read|all
exports.listDoctorAlerts = async (req, res) => {
  try {
    const { status = 'all' } = req.query;
    const params = [];
    let where = '';
    if (status === 'unread') { where = 'WHERE status = \"unread\"'; }
    else if (status === 'read') { where = 'WHERE status = \"read\"'; }

    const [rows] = await db.query(
      `SELECT id, doctor_id, doctor_name, average_rating, message, type, status, created_at
       FROM doctor_notifications
       ${where}
       ORDER BY created_at DESC`
    , params);

    res.json({ success: true, notifications: rows });
  } catch (e) {
    console.error('❌ listDoctorAlerts error:', e);
    res.status(500).json({ error: 'Không thể lấy danh sách cảnh báo' });
  }
};

// GET /api/admin/doctor-alerts/stats
exports.getDoctorAlertStats = async (req, res) => {
  try {
    const [[{ unread = 0 }]] = await db.query("SELECT COUNT(*) AS unread FROM doctor_notifications WHERE status='unread'");
    const [[{ read = 0 }]] = await db.query("SELECT COUNT(*) AS total_read FROM doctor_notifications WHERE status='read'");

    res.json({ success: true, stats: { unread, read } });
  } catch (e) {
    console.error('❌ getDoctorAlertStats error:', e);
    res.status(500).json({ error: 'Không thể lấy thống kê cảnh báo' });
  }
};
